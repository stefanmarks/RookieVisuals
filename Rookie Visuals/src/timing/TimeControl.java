package timing;

/**
 * Interface for adding time control.
 * 
 * @author  Stefan Marks
 */
public interface TimeControl 
{
    /**
     * Gets the start time.
     * 
     * @return  the start time
     */
    public float getStartTime();


    /**
     * Gets the end time.
     * 
     * @return the end time
     */
    public float getEndTime();
}
